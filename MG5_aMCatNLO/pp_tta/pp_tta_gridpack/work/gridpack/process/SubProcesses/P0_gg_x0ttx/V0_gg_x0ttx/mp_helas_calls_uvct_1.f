      INTEGER    NLOOPS, NLOOPGROUPS, NCTAMPS
      PARAMETER (NLOOPS=184, NLOOPGROUPS=89, NCTAMPS=282)
      INTEGER    NLOOPAMPS
      PARAMETER (NLOOPAMPS=466)
      INTEGER    NWAVEFUNCS,NLOOPWAVEFUNCS
