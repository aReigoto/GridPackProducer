      CALL MP_FFV1_0(W(1,8),W(1,4),W(1,6),GC_5,AMPL(1,269))
      AMPL(1,269)=AMPL(1,269)*(2.0D0*UVWFCT_G_2+2.0D0*UVWFCT_G_1+2.0D0
     $ *UVWFCT_T_0)
