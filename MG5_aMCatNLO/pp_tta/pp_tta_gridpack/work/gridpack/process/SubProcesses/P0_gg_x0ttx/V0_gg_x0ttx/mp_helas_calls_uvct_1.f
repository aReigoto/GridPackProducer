      CALL MP_FFV1_0(W(1,5),W(1,7),W(1,6),GC_5,AMPL(2,268))
      AMPL(2,268)=AMPL(2,268)*(2.0D0*UVWFCT_B_0_1EPS+4.0D0
     $ *UVWFCT_G_2_1EPS)
